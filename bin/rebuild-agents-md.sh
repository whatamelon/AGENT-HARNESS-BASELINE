#!/usr/bin/env bash
# rebuild-agents-md.sh
# claude-sync 의 rules/*.md + 사용자 MEMORY.md 인덱스를 합쳐
# Codex(와 외부 도구)가 자동 로드하는 ~/AGENTS.md 를 생성한다.
#
# 사용법:
#   rebuild-agents-md.sh           # 입력이 출력보다 새로우면 빌드, 아니면 skip
#   rebuild-agents-md.sh --force   # 항상 빌드
#   rebuild-agents-md.sh --quiet   # 출력 줄임 (훅에서 호출용)
#
# 트리거:
#   1) bootstrap-new-mac.sh 끝에서 1회
#   2) Claude Code PostToolUse 훅 (rules/MEMORY 수정 감지 시)
#   3) launchd auto-pull 직후 (sync.sh)

set -uo pipefail

CLAUDE_SYNC="$HOME/.config/claude-sync"
RULES_DIR="$CLAUDE_SYNC/claude/rules"
MEMORY_DIR="$HOME/.claude/projects/-Users-denny/memory"
OUTPUT="$HOME/AGENTS.md"

FORCE=0
QUIET=0
for arg in "$@"; do
  case "$arg" in
    --force) FORCE=1 ;;
    --quiet) QUIET=1 ;;
  esac
done

log() { (( QUIET )) || echo "$@"; }

[[ -d "$RULES_DIR" ]] || { log "ℹ️  rules dir 없음 — skip"; exit 0; }

# mtime 비교: 가장 최근 입력 mtime이 출력보다 작으면 skip
if (( ! FORCE )) && [[ -f "$OUTPUT" ]]; then
  out_mtime=$(stat -f %m "$OUTPUT" 2>/dev/null || echo 0)
  newest_input=0
  for f in "$RULES_DIR"/*.md "$MEMORY_DIR/MEMORY.md"; do
    [[ -f "$f" ]] || continue
    m=$(stat -f %m "$f" 2>/dev/null || echo 0)
    (( m > newest_input )) && newest_input=$m
  done
  if (( newest_input <= out_mtime )); then
    log "✓ 최신 — skip"
    exit 0
  fi
fi

# 빌드
TMP=$(mktemp -t agents-md.XXXXXX)
trap "rm -f '$TMP'" EXIT

{
  echo "<!-- AUTO-GENERATED by claude-sync/bin/rebuild-agents-md.sh — DO NOT EDIT -->"
  echo "<!-- 원천: ~/.config/claude-sync/claude/rules/*.md + ~/.claude/projects/.../memory/MEMORY.md -->"
  echo ""
  echo "# 글로벌 컨벤션 (Claude Code ↔ Codex 공유)"
  echo ""
  echo "이 파일은 자동 생성됩니다. 수정하려면 SSOT 파일을 편집하세요:"
  echo ""
  echo "- 규칙: \`~/.config/claude-sync/claude/rules/*.md\`"
  echo "- 메모리 인덱스: \`~/.claude/projects/-Users-denny/memory/MEMORY.md\`"
  echo ""
  echo "프로젝트별 컨벤션은 프로젝트 루트의 \`AGENTS.md\` 가 이 파일을 override 합니다."
  echo ""

  # 규칙 통합
  for rule in "$RULES_DIR"/*.md; do
    [[ -f "$rule" ]] || continue
    echo "---"
    echo ""
    cat "$rule"
    echo ""
  done

  # 메모리 인덱스
  if [[ -f "$MEMORY_DIR/MEMORY.md" ]]; then
    echo "---"
    echo ""
    echo "# 메모리"
    echo ""
    echo "사용자/프로젝트 누적 메모리 인덱스. 각 항목 상세는 \`$MEMORY_DIR/\` 하위 파일 참조."
    echo ""
    cat "$MEMORY_DIR/MEMORY.md"
    echo ""
  fi
} > "$TMP"

mv "$TMP" "$OUTPUT"
trap - EXIT

log "✓ Generated $OUTPUT ($(wc -c < "$OUTPUT" | tr -d ' ') bytes)"
